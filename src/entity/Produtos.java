package entity;

public class Produtos {
	private String id;
	private String nome;
	private String preco;
	private String estoque;
	private String minestoque;
	private String id_fornecedor;
	
	
	
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public String getPreco() {
		return preco;
	}
	public void setPreco(String preco) {
		this.preco = preco;
	}
	public String getEstoque() {
		return estoque;
	}
	public void setEstoque(String estoque) {
		this.estoque = estoque;
	}
	public String getMinestoque() {
		return minestoque;
	}
	public void setMinestoque(String minestoque) {
		this.minestoque = minestoque;
	}
	public String getId_fornecedor() {
		return id_fornecedor;
	}
	public void setId_fornecedor(String id_fornecedor) {
		this.id_fornecedor = id_fornecedor;
	}

	
	
}
